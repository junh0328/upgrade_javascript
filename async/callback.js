'use strict';

/*
    자바스크립트는 동기적인 언어이다.
    호이스팅이 된 후에 코드가 작성한 순서에 맞춰 동기적으로 실행된다는 말이다.
    var, function과 같은 선언들이 코드의 제일 위로 올라가는 것을 호이스팅이라고 한다.

    여기서 비동기 함수란 동기 함수와 다르게 호이스팅된 코드 사이에서 언제 실행될지 모르는 함수를 비동기 함수라고 한다.
    
*/
console.log('1');
setTimeout(function () {
  console.log('2');
}, 1000);
console.log('3');
